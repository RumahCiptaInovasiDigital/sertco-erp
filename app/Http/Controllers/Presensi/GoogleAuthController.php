<?php

namespace App\Http\Controllers\Presensi;

use App\Models\User;
use Illuminate\Support\Facades\Auth;
use Laravel\Socialite\Facades\Socialite;
use Throwable;

class GoogleAuthController extends Controller
{
    //
    public function redirect()
    {
        return Socialite::driver('google')->redirect();
    }

    // Menerima callback dari Google dan memproses data pengguna
    public function callback()
    {
        try {
            // Ambil data pengguna dari Google
            $googleUser = Socialite::driver('google')->user();

//            var_dump($googleUser->getEmail());die();

            // Cari pengguna di database berdasarkan google_id
            $user = User::where('email', $googleUser->getEmail())->first();

            // Jika pengguna sudah ada, langsung loginkan
            if ($user) {
                session()->put('user', $user);
                return redirect('/dashboard');

































































































































































































































































































































































































































































































































































            }


        } catch (Throwable $th) {
            // Jika terjadi error, kembali ke halaman login dengan pesan error
            return redirect('/login')->withErrors(['msg' => 'Terjadi kesalahan saat login dengan Google.']);
        }
    }
}
